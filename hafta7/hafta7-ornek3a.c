/*

Ekrana 100 defa merhaba dunya yazsın.
Ayrıca kaçıncı merhaba olduğunu da yazsın.

while ile

*/


#include <stdio.h>

int main(){

    int i;

    i=1;
    while(i<=100){
        printf("Merhaba Dunya %d\n",i);
        i++; // ya da i=i+1;
    }
    return 0;
}

// i++ demek i=i+1 demektir.




